def _render_sidebar() -> None:
    """Draw the settings sidebar."""
    with st.sidebar:
        st.header("⚙️ Settings")
        st.selectbox(
            "Model",
            options=["IBM Granite 8B", "Baseline — Sentence-BERT", "BM25 (sparse)"],
            help="Display only for now — the demo runs the Granite dense retriever "
                 "+ Granite generator (set GRANITE_MODEL_ID to change the generator).",
            key="model_choice",
        )
        st.slider(
            "Max new tokens",
            min_value=32,
            max_value=1024,
            value=256,
            step=32,
            help="Display only for now — generation uses LLMClient's default config.",
            key="max_tokens",
        )
        st.slider(
            "Top‑k chunks",
            min_value=1,
            max_value=8,
            value=4,
            step=1,
            help="How many retrieved chunks to pass to the generator as context.",
            key="top_k",
        )
        st.divider()
        st.checkbox(
            "📊 Show benchmark comparison",
            value=False,
            key="show_benchmark",
            help="Overlay the system-vs-baselines bar chart from benchmark_results.csv.",
        )


def _render_document_input() -> None:
    """Section 1: document / haystack input."""
    st.subheader("1. Provide a document (haystack)")

    c_left, c_right = st.columns([1, 1])
    with c_left:
        uploaded_file = st.file_uploader(
            "Upload a text document",
            type=["txt", "md"],
            help="Upload is not wired yet — paste the document text below or click "
                 "'Load sample document'.",
            key="uploaded_file",
        )
    with c_right:
        if st.button("📋 Load sample document", use_container_width=True):
            st.session_state.document_text = SAMPLE_DOC

    st.text_area(
        "...or paste text directly",
        height=200,
        placeholder="Paste the long document here, or click 'Load sample document'.",
        key="document_text",
    )


def _set_query(text: str) -> None:
    """Fill the question box. Used as a button ``on_click`` so it runs *before*
    the text_input widget is re-instantiated; setting ``last_query`` inline after
    the widget already exists raises a StreamlitAPIException."""
    st.session_state.last_query = text


def _render_query_input() -> None:
    """Section 2: query input."""
    st.subheader("2. Ask a question (find the needle)")
    col_q, col_btn = st.columns([3, 1])
    with col_q:
        st.text_input(
            "Question",
            placeholder="e.g. What is the secret launch code?",
            key="last_query",
            label_visibility="collapsed",
        )
    with col_btn:
        run = st.button("🔍 Search", type="primary", use_container_width=True)

    # Quick-pick examples.
    st.caption("Try these:")
    examples = [
        "What is the secret launch code?",
        "How did revenue perform in Q4?",
        "Were any security vulnerabilities found?",
        "What accuracy did the ML pipeline achieve?",
        "What was employee satisfaction?",
    ]
    cols = st.columns(len(examples))
    for i, ex in enumerate(examples):
        with cols[i]:
            st.button(
                ex[:28] + "…" if len(ex) > 28 else ex,
                key=f"ex_{i}",
                on_click=_set_query,
                args=(ex,),
            )

    if run and not st.session_state.get("last_query"):
        st.warning("Please enter a question.")
        st.stop()

    return run


def _render_results(chunks: List[RetrievedChunk], answer: str, elapsed: float) -> None:
    """Section 3: RAG results — answer + cited source chunks."""
    st.divider()
    st.subheader("3. Results")
    st.caption(f"Retrieved {len(chunks)} chunks and generated an answer in "
               f"{elapsed:.1f}s.")

    # --- Answer ---
    st.markdown("### 💬 Answer")
    st.markdown(answer)

    # --- Cited sources ---
    if not chunks:
        st.info("No relevant chunks were retrieved from the document.")
        return

    st.markdown("### 📎 Cited sources")
    for i, chunk in enumerate(chunks):
        with st.expander(f"[{i + 1}] {chunk.doc_id}  —  score {chunk.score:.3f}"):
            st.text(chunk.text)


def _render_benchmark_section() -> None:
    """Render the benchmark comparison chart if toggled on."""
    if not st.session_state.get("show_benchmark"):
        return
    st.divider()
    st.subheader("📊 Benchmark: System vs. Baselines")
    df_bench = load_benchmark_data()
    if df_bench is not None:
        render_benchmark_chart(df_bench)
        st.caption(
            "Data from `results/benchmark_results.csv` "
            "(run `python -m eval.run_benchmark` to (re)generate it)."
        )
    else:
        st.warning(
            "`results/benchmark_results.csv` not found. "
            "Run `python -m eval.run_benchmark` first, or create mock data."
        )


# ---------------------------------------------------------------------------
# Main entry point
# ---------------------------------------------------------------------------

def main() -> None:
    """Render the Streamlit demo app."""
    st.set_page_config(
        page_title="Needle in a Haystack — IBM Granite",
        page_icon="🪡",
        layout="wide",
    )
    _init_session()

    st.title("🪡 Needle in a Haystack — IBM Granite")
    st.caption(
        "Retrieve relevant passages, generate a grounded answer with citations "
        "(RAG), and compare retriever performance — all in one demo."
    )

    _render_sidebar()
    _render_document_input()
    triggered = _render_query_input()

    # --- Run the real retrieve-then-generate pipeline ----------------- #
    if triggered:
        question = st.session_state.last_query
        document_text = st.session_state.get("document_text", "")
        top_k = st.session_state.get("top_k", 4)

        if not document_text.strip():
            st.warning("Please provide a document first — paste text above or click "
                       "'Load sample document'.")
            st.stop()

        t0 = time.perf_counter()
        try:
            with st.spinner("Indexing your document (first run downloads the Granite "
                            "embedding model)..."):
                pipeline = _get_pipeline(document_text, top_k)
            with st.spinner("Retrieving and generating with Granite..."):
                result = pipeline.query(question)
        except Exception as exc:  # noqa: BLE001 - surface a friendly UI error
            st.error(
                "The pipeline failed to run. On the first run the Granite models "
                "download from Hugging Face, which can take a while.\n\n"
                f"Details: {exc}"
            )
            st.stop()
        elapsed = time.perf_counter() - t0
        _render_results(result.retrieved_chunks, result.answer, elapsed)

    # --- Benchmark overlay (always available) ------------------------- #
    _render_benchmark_section()


if __name__ == "__main__":
    main()
