"""Streamlit demo for the IBM Granite retrieval + RAG system.

A lightweight UI to demonstrate the retrieve-then-generate pipeline (the A+B
deliverable) with IBM Granite. The user can:

- Paste a document (or load the sample).
- Type a question (search box).
- Run the query and view the model's answer **plus the source chunks it cited**
  (the real ``RAGPipeline`` -> ``RAGResult`` flow over a Granite dense retriever
  and a Granite generative model).
- Toggle a benchmark comparison chart to see Granite vs. baselines.

Run from the project root:

    streamlit run app/main.py

This wires the **real** pipeline: the pasted document is chunked, embedded with a
Granite embedding model, indexed, and retrieved (``src.retrieval.factory``); the
answer is generated by a local Granite model (``src.llm_client.LLMClient``) via
``src.rag_pipeline.RAGPipeline``. The generative model defaults to the lightweight
``granite-4.1-3b`` (CPU-friendly; set ``GRANITE_MODEL_ID`` / ``LLM_DEVICE`` to
change). Both models download from Hugging Face on first use and are cached across
reruns with ``st.cache_resource``.
"""

from __future__ import annotations

import sys
import time
from pathlib import Path
from typing import List

import matplotlib.pyplot as plt
import pandas as pd
import seaborn as sns
import streamlit as st

# Make the project root importable when launched via `streamlit run app/main.py`
# (Streamlit puts app/ on sys.path, not the repo root).
sys.path.insert(0, str(Path(__file__).resolve().parent.parent))

from src.llm_client import LLMClient  # noqa: E402
from src.rag_app import build_rag_pipeline_from_text  # noqa: E402
from src.retrieval.base import RetrievedChunk  # noqa: E402


# ---------------------------------------------------------------------------
# Real retrieve-then-generate pipeline (cached so models load once)
# ---------------------------------------------------------------------------

@st.cache_resource(show_spinner=False)
def _get_llm():
    """Load the Granite generative model once (cached across reruns).

    Model id comes from ``GRANITE_MODEL_ID`` (default ``granite-4.1-3b``) and the
    device from ``LLM_DEVICE`` — see ``src/llm_client.py`` / ``.env.example``.
    """
    return LLMClient()


@st.cache_resource(show_spinner=False)
def _get_pipeline(document_text: str, top_k: int):
    """Build (and cache) a RAG pipeline over ``document_text``.

    Keyed by the document text and ``top_k``: pasting a new document rebuilds the
    index, the same document reuses it. The generative model is shared via
    :func:`_get_llm`.
    """
    return build_rag_pipeline_from_text(document_text, _get_llm(), top_k=top_k)


# ---------------------------------------------------------------------------
# Benchmark data loader + chart
# ---------------------------------------------------------------------------

def _resolve_results_dir() -> Path:
    """Find the ``results/`` directory relative to the project root."""
    candidates = [
        Path("results"),                  # running from project root
        Path(__file__).resolve().parent.parent / "results",  # from app/
    ]
    for p in candidates:
        if p.exists():
            return p
    return candidates[0]  # fallback — will show a clear error downstream


def load_benchmark_data() -> pd.DataFrame | None:
    """Load benchmark_results.csv if available, else return None."""
    csv_path = _resolve_results_dir() / "benchmark_results.csv"
    if csv_path.exists():
        return pd.read_csv(csv_path)
    return None


def render_benchmark_chart(df: pd.DataFrame) -> None:
    """Draw the system-vs-baselines grouped bar chart inline."""
    headline = ["precision@10", "recall@10", "ndcg@10", "mrr"]
    available = [m for m in headline if m in df.columns]
    if not available:
        st.warning("No headline metrics found in benchmark_results.csv.")
        return

    df_long = df.melt(
        id_vars=["retriever"],
        value_vars=available,
        var_name="metric",
        value_name="score",
    )

    sns.set_theme(style="white", context="notebook")
    fig, ax = plt.subplots(figsize=(8, 5))
    palette = {"bm25": "#b0b0b0", "st_dense": "#7eb0d5", "granite_dense": "#8a5cc4"}
    # Only use colours for models actually present.
    present_models = [m for m in palette if m in df["retriever"].values]
    sns.barplot(
        data=df_long,
        x="metric",
        y="score",
        hue="retriever",
        palette={m: palette[m] for m in present_models},
        ax=ax,
    )
    ax.set_title("Retrieval Benchmark: Granite Dense vs. Baselines (SciFact)",
                 fontsize=12, weight="bold")
    ax.set_xlabel("")
    ax.set_ylabel("Score")
    ax.set_ylim(0, 1.0)
    ax.legend(title="Retriever", frameon=True)
    ax.grid(axis="y", alpha=0.3)
    plt.tight_layout()
    st.pyplot(fig)
    plt.close(fig)


