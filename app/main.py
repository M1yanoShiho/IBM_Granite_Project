def _init_session() -> None:
    """One-time initialisation of Streamlit session state."""
    defaults = {
        "document_text": "",
        "last_query": "",
        "last_answer": "",
        "last_chunks": [],
        "show_results": False,
    }
    for key, val in defaults.items():
        if key not in st.session_state:
            st.session_state[key] = val


